package utils

import "strings"

// CanonicalDomain нормализует доменное имя к единственной канонической форме,
// используемой во всём приложении: запись в block_lists, bloom-фильтр,
// LRU-кэш вердиктов и горячий путь DNS-запроса.
//
// Форма — нижний регистр, без окружающих пробелов, с ровно одной точкой в
// конце (FQDN). Это нужно, чтобы запись «example.com», пришедшая через
// POST /api/dns-records/create, совпала с FQDN-именем «example.com.», которое
// miekg/dns передаёт на горячем пути (регистр там тоже не гарантирован —
// клиенты используют 0x20-кодирование).
//
// Пустой вход, строка из одних пробелов или из одних точек дают "" —
// вызывающий трактует это как «домена нет». Функция идемпотентна.
//
// Ограничение: IDNA/Punycode не выполняется. strings.ToLower понижает
// регистр Unicode, но не приводит «пример.рф» к «xn--e1afmkfd.xn--p1ai».
// Если на горячем пути miekg/dns отдаст имя в Punycode, а в блок-лист его
// добавили в Unicode (или наоборот) — формы не совпадут. Для текущих
// источников (ASCII-домены) это не проблема; полноценная IDN-поддержка —
// отдельная задача.
func CanonicalDomain(s string) string {
	s = strings.Trim(strings.ToLower(strings.TrimSpace(s)), ".")
	if s == "" {
		return ""
	}
	return s + "."
}
